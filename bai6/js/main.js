function run() {
  var canvas = document.getElementById('mycanvas');
  ctx = canvas.getContext('2d');
  redraw();
  document.body.onkeydown = function(e){
    if (e.keyCode === 40) {
      moveDown();
    }
    if (e.keyCode === 38) {
      moveUp();
    }
    if (e.keyCode === 39) {
      moveRight();
    }
    if (e.keyCode === 37) {
      moveLeft();
    }
  };
}

function redraw() {
  ctx.fillStyle = 'rgba(255, 255, 255)';
  ctx.fillRect(0, 0, 800, 800);
  ctx.fillStyle = 'rgba(0, 0, 200, 1)';
  ctx.fillRect(0, 0, 800, 800);

  drawBackground(ctx);
  drawT(ctx, x, y);
  var debug = document.getElementById('debug');
  debug.innerHTML = "X: " + x + "  Y:" + y;
}

function moveRight() {
  x += 1;
  redraw();
}
function moveLeft() {
  x -= 1;
  redraw();
}
function moveDown() {
  y += 1;
  redraw();
}
function moveUp() {
  y -= 1;
  redraw();
}

function drawT(ctx, x, y) {
  ctx.fillStyle = 'rgb(200, 0, 0)';
  var tBick = [
    [1,1,1],
    [0,1,0],
    [0,1,0],
  ]
  for (var dong = 0; dong <= 2; dong++) {
    for( var cot = 0; cot <= 2; cot ++) {
      var value = tBick[dong][cot];

      var pointX = x + cot * CELL_UNIT + 10;
      var pointY = y + dong * CELL_UNIT + 10;
      ctx.fillText(value, pointX, pointY);
    }
  }
}

function drawL(ctx, x, y) {
  ctx.fillStyle = 'rgb(200, 0, 0)';
  var tBick = [
    [1,0,0],
    [1,0,0],
    [1,1,0],
  ]
  for (var i = 0; i < 3; i++) {
    for (var j = 0; j < 3; j++) {
      var value = tBick[i][j];
      var pointX = x + j * CELL_UNIT;
      var pointY = y + i * CELL_UNIT;
      if (value === 1) {
        ctx.fillText(value, pointX, pointY);
      }
    }
  }
}

function drawBackground(ctx) {
  ctx.fillStyle = 'rgb(200, 200, 0)';
  for (var dong = 0; dong < background.length; dong++) {
    for (var cot = 0; cot < background[dong].length; cot++) {
      var value = background[dong][cot];
      var pointX = cot * CELL_UNIT + 10;
      var pointY = dong * CELL_UNIT + 10;
      ctx.fillText(value, pointX, pointY);
    }
  }
}
